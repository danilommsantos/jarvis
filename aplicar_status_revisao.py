# --- 2. Identificar as revisoes ---------------------------------------------

# Devolvidos: RevisaoProcesso cujo numero esta na lista
qs_devolvidos = RevisaoProcesso.objects.filter(
    processo__numero__in=DEVOLVIDOS
).prefetch_related("status")

# Liberados: todas as RevisaoProcesso que nao tem nenhum status
qs_liberados = RevisaoProcesso.objects.filter(
    status__isnull=True
).exclude(
    processo__numero__in=DEVOLVIDOS
)

